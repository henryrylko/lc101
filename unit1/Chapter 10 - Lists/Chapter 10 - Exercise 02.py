#  Create a list called my_list with the following six items:
#  76, 92.3, "hello", True, 4, 76.  Do it with both append and with
#  concatenation, one item at a time.

my_list_append = []
my_list_append.append(76)
my_list_append.append(92.3)
my_list_append.append("hello")
my_list_append.append(True)
my_list_append.append(4)
my_list_append.append(76)
print(my_list_append)

my_list_concatenate = []
my_list_concatenate += [76]
my_list_concatenate += [92.3]
my_list_concatenate += ["hello"]
my_list_concatenate += [True]
my_list_concatenate += [4]
my_list_concatenate += [76]
print(my_list_concatenate)
